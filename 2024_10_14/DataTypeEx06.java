public class DataTypeEx06 {
    public static void main(String[] args) {
        // 실수
        // float, double(*)

        float f = 2.5f;
        System.out.println(f);

        double d = 2.5;
        System.out.println(d);

        // 정수 => 실수 = 지수
        // 1000 -> 1.0e3
        double d2 = 1.0e3;
        System.out.println(d2);
    }
}