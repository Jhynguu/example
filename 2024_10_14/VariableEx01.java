public class VariableEx01 {
    public static void main(String[] args) {
        // 변수 선언
        // 자료형 변수명
        // 정수형 자료형 num1 변수명
        int num1;

        // 초기화
        // = ( 오른쪽 -> 왼쪽)
        num1 = 10;
        // 10 = num1;

        // 선언과 동시에 초기화
        int num2 = 20;

        // 변수 내용 출력
        System.out.println(num1);
        System.out.println(num2);
    }
}